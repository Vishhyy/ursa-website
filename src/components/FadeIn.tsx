"use client";
import { motion } from "framer-motion";

export default function FadeIn({
    children,
    delay = 0,
    className = ""
}: {
    children: React.ReactNode;
    delay?: number;
    className?: string;
}) {
    return (
        <motion.div
            initial={{ opacity: 0, y: 15 }} // Reduced distance (was 20)
            animate={{ opacity: 1, y: 0 }}  // Changed from whileInView to animate
            transition={{
                duration: 0.5,
                delay: delay,
                ease: "easeOut"
            }}
            className={`h-full ${className}`}
        >
            {children}
        </motion.div>
    );
}